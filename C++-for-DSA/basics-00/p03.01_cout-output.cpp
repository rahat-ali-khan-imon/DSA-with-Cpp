#include<iostream>
using namespace std;

int main() {
    int num1 = 10, num2 = 40;

    cout << num1 << endl;
    cout << num2 << endl;
    cout << num1 << endl << num2 << endl;
    cout << num1 << "\t" << num2 << endl;
    cout << num1 << " " << num2 << endl;
    cout << "500\t400" << endl;
    cout << 700 << endl;
    cout << 700 + 300;

    return 0;
}